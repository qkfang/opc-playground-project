/* Northwind Revenue Origination Console — proj40
   Drives the 5 flow views against the minimal API:
     /api/inbox, /api/cases, /api/cases/from-inbox/{id}, /api/cases (POST), /api/cases/run-demo, /api/health
*/
(() => {
  "use strict";

  const $ = (sel, root = document) => root.querySelector(sel);
  const $$ = (sel, root = document) => Array.from(root.querySelectorAll(sel));
  const esc = (s) => String(s ?? "").replace(/[&<>"']/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c]));
  const money = (n) => "$" + Number(n || 0).toLocaleString("en-US", { maximumFractionDigits: 0 });

  const state = { inbox: [], cases: [], current: null, selectedEmailId: null, engine: "…" };

  const VIEWS = {
    inbox:   { title: "Intake Inbox",        sub: "Inbound demand routed through the multi-agent origination pipeline." },
    records: { title: "Extracted Records",   sub: "Lead, Account and Opportunity entities extracted from the inbound email." },
    triage:  { title: "Triage & Routing",    sub: "Classification, lead scoring, prioritisation and routing rationale." },
    research:{ title: "Lead Research",        sub: "Firmographic research and inbound demand signals from the Lead Management agent." },
    report:  { title: "Origination Brief",   sub: "Executive study generated by the reporting agent." },
  };

  // ---------------- API ----------------
  async function api(path, opts) {
    const res = await fetch(path, opts);
    if (!res.ok) {
      let detail = "";
      try { detail = JSON.stringify(await res.json()); } catch { detail = res.statusText; }
      throw new Error(`${res.status} ${detail}`);
    }
    return res.json();
  }

  // ---------------- Toast ----------------
  let toastTimer;
  function toast(msg, kind = "") {
    const t = $("#toast");
    t.textContent = msg;
    t.className = "toast show " + kind;
    clearTimeout(toastTimer);
    toastTimer = setTimeout(() => (t.className = "toast " + kind), 2600);
  }

  // ---------------- Navigation ----------------
  function setView(view) {
    $$(".nav-item").forEach((b) => b.classList.toggle("active", b.dataset.view === view));
    $$(".view").forEach((v) => v.classList.add("hidden"));
    $("#view-" + view).classList.remove("hidden");
    $("#viewTitle").textContent = VIEWS[view].title;
    $("#viewSubtitle").textContent = VIEWS[view].sub;
    if (view !== "inbox") renderCurrent(view);
  }

  // ---------------- Stepper ----------------
  function setStepper(c) {
    const stages = c ? new Set(c.trace.map((t) => t.stage)) : new Set();
    $$(".step").forEach((s) => s.classList.toggle("done", stages.has(s.dataset.step)));
  }

  // ---------------- Engine pill ----------------
  function setEngine(name, configured) {
    state.engine = name;
    const pill = $("#enginePill");
    pill.classList.toggle("live", name === "foundry");
    pill.classList.toggle("offline", name !== "foundry");
    $("#engineLabel").textContent = name === "foundry" ? "Foundry agents (live)" : "Offline agents";
  }

  // ---------------- Inbox ----------------
  function renderInbox() {
    const list = $("#inboxList");
    $("#inboxCount").textContent = state.inbox.length;
    if (!state.inbox.length) { list.innerHTML = '<div class="empty">Inbox empty.</div>'; return; }
    list.innerHTML = state.inbox.map((e) => `
      <div class="mail ${state.selectedEmailId === e.id ? "sel" : ""}" data-id="${esc(e.id)}">
        <div class="mail-top"><span class="mail-from">${esc(e.fromName || e.from)}</span><span class="chan">${esc(e.channel)}</span></div>
        <div class="mail-subj">${esc(e.subject)}</div>
        <div class="mail-snip">${esc(e.body)}</div>
      </div>`).join("");
    $$(".mail", list).forEach((m) => m.addEventListener("click", () => selectEmail(m.dataset.id)));
  }

  function selectEmail(id) {
    state.selectedEmailId = id;
    const e = state.inbox.find((x) => x.id === id);
    renderInbox();
    if (!e) return;
    $("#emailPreview").innerHTML = `
      <div class="ep-subj">${esc(e.subject)}</div>
      <div class="muted small">${esc(e.fromName || "")} &lt;${esc(e.from)}&gt; · ${esc(e.channel)}</div>
      <div class="ep-body">${esc(e.body)}</div>
      <button class="btn primary" id="btnRunSelected" style="margin-top:10px;">▶ Process this email</button>`;
    $("#btnRunSelected").addEventListener("click", () => processInbox(id));
  }

  // ---------------- Cases table ----------------
  function pillFor(priority) {
    const p = (priority || "").toLowerCase();
    return `<span class="pill ${p === "p1" ? "p1" : p === "p2" ? "p2" : "p3"}">${esc(priority || "—")}</span>`;
  }

  function renderCases() {
    $("#caseCount").textContent = state.cases.length;
    const tb = $("#casesTable tbody");
    if (!state.cases.length) { tb.innerHTML = '<tr><td colspan="8" class="empty">No cases yet — process an email.</td></tr>'; return; }
    tb.innerHTML = state.cases.map((c) => `
      <tr data-id="${esc(c.caseId)}">
        <td><code>${esc(c.caseId)}</code></td>
        <td>${esc(c.company)}</td>
        <td>${esc(c.segment)}</td>
        <td>${esc(c.classification)}</td>
        <td>${pillFor(c.priority)}</td>
        <td><strong>${esc(c.leadScore)}</strong></td>
        <td>${money(c.arr)}</td>
        <td><span class="pill ${c.engine === "foundry" ? "ok" : "neu"}">${esc(c.engine)}</span></td>
      </tr>`).join("");
    $$("#casesTable tbody tr[data-id]").forEach((tr) =>
      tr.addEventListener("click", () => openCase(tr.dataset.id)));
  }

  async function openCase(caseId) {
    try {
      const c = await api(`/api/cases/${caseId}`);
      state.current = c;
      setStepper(c);
      setView("records");
    } catch (e) { toast("Could not load case: " + e.message, "bad"); }
  }

  // ---------------- Process ----------------
  async function processInbox(id) {
    const btn = $("#btnRunSelected");
    if (btn) { btn.disabled = true; btn.textContent = "Processing…"; }
    try {
      const c = await api(`/api/cases/from-inbox/${id}`, { method: "POST" });
      onProcessed(c);
    } catch (e) { toast("Pipeline error: " + e.message, "bad"); }
    finally { if (btn) { btn.disabled = false; btn.textContent = "▶ Process this email"; } }
  }

  async function processAdhoc(ev) {
    ev.preventDefault();
    const f = ev.target;
    const payload = {
      from: f.from.value.trim(),
      fromName: "",
      subject: f.subject.value.trim(),
      body: f.body.value.trim(),
      channel: "email",
    };
    if (!payload.from && !payload.body) { toast("Enter a from address or body.", "bad"); return; }
    const btn = f.querySelector('button[type="submit"]');
    btn.disabled = true; btn.textContent = "Processing…";
    try {
      const c = await api("/api/cases", { method: "POST", headers: { "Content-Type": "application/json" }, body: JSON.stringify(payload) });
      onProcessed(c);
      f.reset();
    } catch (e) { toast("Pipeline error: " + e.message, "bad"); }
    finally { btn.disabled = false; btn.textContent = "Run origination pipeline"; }
  }

  async function onProcessed(c) {
    state.current = c;
    setStepper(c);
    await refreshCases();
    toast(`Case ${c.caseId} processed via ${c.engine} — ${c.triage.classification}/${c.triage.priority}`, "ok");
    setView("records");
  }

  async function runDemo() {
    const btn = $("#btnRunDemo");
    btn.disabled = true; btn.textContent = "Running…";
    try {
      const r = await api("/api/cases/run-demo", { method: "POST" });
      await refreshCases();
      toast(`Processed ${r.processed} inbox emails via ${r.engine}.`, "ok");
    } catch (e) { toast("Run failed: " + e.message, "bad"); }
    finally { btn.disabled = false; btn.textContent = "▶ Run full inbox"; }
  }

  async function resetCases() {
    if (!confirm("Clear all processed cases?")) return;
    try { await api("/api/cases", { method: "DELETE" }); state.current = null; setStepper(null); await refreshCases(); toast("Cases cleared.", "ok"); }
    catch (e) { toast("Reset failed: " + e.message, "bad"); }
  }

  // ---------------- Renderers for detail views ----------------
  function needCurrent(view) {
    const host = $("#view-" + view);
    host.innerHTML = `<div class="card"><div class="placeholder"><div class="big">🗂️</div>
      No case selected yet.<br/><span class="small">Process an email from the Intake Inbox to populate this view.</span></div></div>`;
  }

  function bar(score, invert) {
    const cls = score >= 70 ? "green" : score >= 45 ? "amber" : "red";
    return `<div class="bar ${cls}"><span style="width:${Math.max(3, Math.min(100, score))}%"></span></div>`;
  }

  function renderRecords(c) {
    const r = c.records, L = r.lead, A = r.account, O = r.opportunity;
    const confCls = r.confidence >= 70 ? "ok" : r.confidence >= 50 ? "warn" : "bad";
    $("#view-records").innerHTML = `
      <div class="card">
        <div class="card-head"><h2>Case ${esc(c.caseId)} · ${esc(A.companyName)}</h2>
          <span class="pill ${confCls}">Extraction confidence ${esc(r.confidence)}%</span></div>
        <div class="grid-3">
          <div class="entity"><h3>Lead</h3><div class="kv">
            <span class="k">Name</span><span class="v">${esc(L.fullName) || "—"}</span>
            <span class="k">Title</span><span class="v">${esc(L.title) || "—"}</span>
            <span class="k">Seniority</span><span class="v">${esc(L.seniority)}</span>
            <span class="k">Decision-maker</span><span class="v">${L.isDecisionMaker ? "✅ Yes" : "—"}</span>
            <span class="k">Email</span><span class="v">${esc(L.email)}</span>
            <span class="k">Phone</span><span class="v">${esc(L.phone) || "—"}</span>
          </div></div>
          <div class="entity"><h3>Account</h3><div class="kv">
            <span class="k">Company</span><span class="v">${esc(A.companyName)}</span>
            <span class="k">Industry</span><span class="v">${esc(A.industry)}</span>
            <span class="k">Segment</span><span class="v">${esc(A.segment)}</span>
            <span class="k">Employees</span><span class="v">${esc(A.employeeBand) || "—"}</span>
            <span class="k">Region</span><span class="v">${esc(A.region) || "—"} ${esc(A.country ? "· " + A.country : "")}</span>
            <span class="k">Customer</span><span class="v">${A.isExistingCustomer ? "Existing" : "Net-new"}</span>
          </div></div>
          <div class="entity"><h3>Opportunity</h3><div class="kv">
            <span class="k">Interest</span><span class="v">${esc(O.productInterest)}</span>
            <span class="k">Est. ARR</span><span class="v"><strong>${money(O.estimatedAnnualValue)}</strong></span>
            <span class="k">Timeline</span><span class="v">${esc(O.timeline)}</span>
            <span class="k">Budget</span><span class="v">${esc(O.budgetStatus)}</span>
            <span class="k">Competitors</span><span class="v">${O.competitors && O.competitors.length ? O.competitors.map(esc).join(", ") : "—"}</span>
          </div></div>
        </div>
        ${O.useCase ? `<p class="muted small" style="margin-top:12px;"><strong>Use case:</strong> ${esc(O.useCase)}</p>` : ""}
        ${r.missingFields && r.missingFields.length ? `<p class="small" style="margin-top:8px;">Needs enrichment: ${r.missingFields.map((m) => `<span class="chip risk">${esc(m)}</span>`).join("")}</p>` : ""}
      </div>
      ${traceCard(c)}`;
  }

  function renderTriage(c) {
    const t = c.triage;
    const pcls = (t.priority || "").toLowerCase();
    $("#view-triage").innerHTML = `
      <div class="grid-3">
        <div class="card"><div class="metric"><div class="num">${esc(t.leadScore)}</div><div class="lbl">Lead score / 100</div>${bar(t.leadScore)}</div></div>
        <div class="card"><div class="metric"><div class="num"><span class="pill ${pcls === "p1" ? "p1" : pcls === "p2" ? "p2" : "p3"}" style="font-size:20px;padding:4px 14px;">${esc(t.priority)}</span></div><div class="lbl">Priority · SLA ${esc(t.slaHours)}h</div></div></div>
        <div class="card"><div class="metric"><div class="num" style="font-size:20px;">${esc(t.classification)}</div><div class="lbl">Classification</div>${t.disqualified ? '<span class="pill bad" style="margin-top:8px;">Disqualified</span>' : '<span class="pill ok" style="margin-top:8px;">Qualified</span>'}</div></div>
      </div>
      <div class="card">
        <div class="card-head"><h2>Routing & recommended action</h2></div>
        <div class="kv" style="grid-template-columns:160px 1fr;">
          <span class="k">Routing queue</span><span class="v">${esc(t.routingQueue)}</span>
          <span class="k">Recommended action</span><span class="v">${esc(t.recommendedAction)}</span>
        </div>
        <p class="muted small" style="margin-top:12px;"><strong>Rationale:</strong> ${esc(t.rationale)}</p>
        ${t.riskFlags && t.riskFlags.length ? `<div style="margin-top:8px;">${t.riskFlags.map((f) => `<span class="chip risk">⚠ ${esc(f)}</span>`).join("")}</div>` : '<p class="small"><span class="pill ok">No risk flags</span></p>'}
      </div>
      ${traceCard(c)}`;
  }

  function renderResearch(c) {
    const r = c.research;
    $("#view-research").innerHTML = `
      <div class="grid-3">
        <div class="card" style="grid-column: span 2;">
          <div class="card-head"><h2>Account overview</h2></div>
          <p>${esc(r.companyOverview)}</p>
          <h4 class="small" style="color:var(--brand-ink);margin:10px 0 4px;text-transform:uppercase;letter-spacing:.5px;">Likely initiatives</h4>
          <ul class="clean">${(r.keyInitiatives || []).map((i) => `<li>• ${esc(i)}</li>`).join("")}</ul>
        </div>
        <div class="card"><div class="metric"><div class="num">${esc(r.intentScore)}</div><div class="lbl">Buying intent / 100</div>${bar(r.intentScore)}<div style="margin-top:10px;"><span class="pill ${r.buyingStage === "Decision" ? "ok" : r.buyingStage === "Consideration" ? "warn" : "neu"}">${esc(r.buyingStage)} stage</span></div></div></div>
      </div>
      <div class="card">
        <div class="card-head"><h2>Inbound demand signals</h2><span class="badge">${(r.signals || []).length}</span></div>
        ${(r.signals || []).map((s) => `
          <div class="signal">
            <div class="signal-top"><h4>${esc(s.title)}</h4><span class="cat">${esc(s.category)}</span></div>
            <div class="det">${esc(s.detail)}</div>
            <div class="meta">Strength ${esc(s.strength)} · ${esc(s.recency)} · ${esc(s.source)}</div>
          </div>`).join("") || '<div class="empty">No signals.</div>'}
        <h4 class="small" style="color:var(--brand-ink);margin:12px 0 4px;text-transform:uppercase;letter-spacing:.5px;">Talking points</h4>
        <ul class="clean">${(r.talkingPoints || []).map((p) => `<li>💬 ${esc(p)}</li>`).join("")}</ul>
      </div>
      ${traceCard(c)}`;
  }

  function renderReport(c) {
    const rep = c.report;
    $("#view-report").innerHTML = `
      <div class="card report">
        <div class="card-head"><h2>${esc(rep.title)}</h2><span class="pill ${c.engine === "foundry" ? "ok" : "neu"}">${esc(rep.generatedBy || c.engine)}</span></div>
        <div class="exec">${esc(rep.executiveSummary)}</div>
        ${rep.highlights && rep.highlights.length ? `<div class="sec"><h4>Highlights</h4><ul>${rep.highlights.map((h) => `<li>${esc(h)}</li>`).join("")}</ul></div>` : ""}
        ${(rep.sections || []).map((s) => `<div class="sec"><h4>${esc(s.heading)}</h4><p>${esc(s.body)}</p></div>`).join("")}
        ${rep.recommendations && rep.recommendations.length ? `<div class="sec"><h4>Recommendations</h4><ul>${rep.recommendations.map((x) => `<li>${esc(x)}</li>`).join("")}</ul></div>` : ""}
        <div class="nba">▶ Next best action: ${esc(rep.nextBestAction)}</div>
      </div>
      ${traceCard(c)}`;
  }

  function traceCard(c) {
    return `<div class="card">
      <div class="card-head"><h2>Agent pipeline trace</h2><span class="pill ${c.engine === "foundry" ? "ok" : "neu"}">${esc(c.engine)} engine</span></div>
      <div class="trace">${(c.trace || []).map((t) => `
        <div class="trace-item">
          <span class="trace-stage">${esc(t.stage)} <span class="trace-agent">(${esc(t.agent)})</span></span>
          <span>${esc(t.summary)}</span>
          <span class="muted small">${t.durationMs != null ? esc(t.durationMs) + " ms" : ""}</span>
        </div>`).join("")}</div>
    </div>`;
  }

  function renderCurrent(view) {
    if (!state.current) { needCurrent(view); return; }
    const c = state.current;
    if (view === "records") renderRecords(c);
    else if (view === "triage") renderTriage(c);
    else if (view === "research") renderResearch(c);
    else if (view === "report") renderReport(c);
  }

  // ---------------- Data refresh ----------------
  async function refreshCases() {
    try { state.cases = await api("/api/cases"); renderCases(); } catch (e) { /* keep prior */ }
  }

  async function init() {
    // Wire navigation.
    $$(".nav-item").forEach((b) => b.addEventListener("click", () => setView(b.dataset.view)));
    $("#btnRunDemo").addEventListener("click", runDemo);
    $("#btnReset").addEventListener("click", resetCases);
    $("#adhocForm").addEventListener("submit", processAdhoc);

    try {
      const health = await api("/api/health");
      setEngine(health.engine, health.foundryConfigured);
    } catch { setEngine("offline", false); }

    try { state.inbox = await api("/api/inbox"); renderInbox(); } catch (e) { $("#inboxList").innerHTML = '<div class="empty">Could not load inbox.</div>'; }
    await refreshCases();
    if (state.inbox.length) selectEmail(state.inbox[0].id);
  }

  document.addEventListener("DOMContentLoaded", init);
})();
